package dam.prog1.UT8.excepciones.Pandilla;

public class Amigo {
	String nombre;
	String apodo;
	public Amigo(String nombre, String apodo) {
		
		this.nombre = nombre;
		this.apodo = apodo;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getApodo() {
		return apodo;
	}
	public void setApodo(String apodo) {
		this.apodo = apodo;
	}
	
	
}
